-- stock_items.company_id -> companies.company_id
-- EXPLICIT in source (REFERENCES companies(company_id) ON DELETE CASCADE).
ALTER TABLE stock_items
    ADD CONSTRAINT fk_stock_items_company
    FOREIGN KEY (company_id) REFERENCES companies (company_id)
    ON DELETE CASCADE;

-- stock_items.group_id -> stock_groups.sg_id
-- EXPLICIT in source (REFERENCES stock_groups(sg_id)).
ALTER TABLE stock_items
    ADD CONSTRAINT fk_stock_items_group
    FOREIGN KEY (group_id) REFERENCES stock_groups (sg_id);

-- stock_items.category_id -> stock_categories.sc_id
-- EXPLICIT in source (REFERENCES stock_categories(sc_id)).
ALTER TABLE stock_items
    ADD CONSTRAINT fk_stock_items_category
    FOREIGN KEY (category_id) REFERENCES stock_categories (sc_id);

-- stock_items.unit_id -> units.unit_id
-- EXPLICIT in source (REFERENCES units(unit_id)).
ALTER TABLE stock_items
    ADD CONSTRAINT fk_stock_items_unit
    FOREIGN KEY (unit_id) REFERENCES units (unit_id);

-- stock_item_opening_allocations.item_id -> stock_items.item_id
-- EXPLICIT in source (REFERENCES stock_items(item_id) ON DELETE CASCADE).
ALTER TABLE stock_item_opening_allocations
    ADD CONSTRAINT fk_sioa_item
    FOREIGN KEY (item_id) REFERENCES stock_items (item_id)
    ON DELETE CASCADE;

-- stock_item_opening_allocations.godown_id -> godowns.godown_id
-- EXPLICIT in source (REFERENCES godowns(godown_id) ON DELETE SET NULL).
ALTER TABLE stock_item_opening_allocations
    ADD CONSTRAINT fk_sioa_godown
    FOREIGN KEY (godown_id) REFERENCES godowns (godown_id)
    ON DELETE SET NULL;
