-- ----------------------------------------------------------------------------
-- Table: stock_item_opening_allocations
-- ----------------------------------------------------------------------------
CREATE TABLE stock_item_opening_allocations (
    allocation_id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    item_id         BIGINT       NOT NULL,                                    -- FK -> stock_items.item_id

    godown_id       BIGINT,                                                   -- FK -> godowns.godown_id (ON DELETE SET NULL)
    batch_number    TEXT,
    mfg_date        DATE,                                                     -- date-only text in SQLite
    expiry_date     DATE,                                                     -- date-only text in SQLite

    quantity        NUMERIC(18,4) NOT NULL DEFAULT 0,                         -- quantity
    rate            NUMERIC(18,2) NOT NULL DEFAULT 0,                         -- MONEY per unit (do NOT use float)
    amount          NUMERIC(18,2) NOT NULL DEFAULT 0                          -- MONEY = quantity * rate (computed in app)
);

COMMENT ON COLUMN stock_item_opening_allocations.rate   IS 'MONEY per unit. Mapped from SQLite REAL to NUMERIC(18,2). Never use floating point for money.';
COMMENT ON COLUMN stock_item_opening_allocations.amount IS 'MONEY. Computed in app as quantity * rate. NUMERIC(18,2).';

CREATE INDEX idx_sioa_item_id   ON stock_item_opening_allocations (item_id);
CREATE INDEX idx_sioa_godown_id ON stock_item_opening_allocations (godown_id);


-- ============================================================================
-- Foreign key constraints (grouped to avoid cross-module ordering issues)
