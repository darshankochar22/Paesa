    report_id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id                 BIGINT       NOT NULL,
    report_name                TEXT         DEFAULT 'Day Book',
    date_from                  DATE,                       -- date-only ISO text in source
    date_to                    DATE,                       -- date-only ISO text in source
    selected_company_id        BIGINT,
    basis_of_values            TEXT         DEFAULT 'Default',
    change_view                TEXT,
    exception_reports_enabled  BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    saved_view_name            TEXT,
    filter_enabled             BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    filter_details             TEXT,
    show_profit                BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    show_columnar              BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    show_optional              BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    show_post_dated            BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    show_stat_adjustment       BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    show_details               BOOLEAN      NOT NULL DEFAULT true,   -- SQLite INTEGER 0/1, default 1
    show_related_reports       BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    created_at                 TIMESTAMPTZ  NOT NULL DEFAULT now(),  -- ISO string in source
    updated_at                 TIMESTAMPTZ  NOT NULL DEFAULT now()   -- ISO string in source
);

-- ----------------------------------------------------------------------------
-- day_book_entries  (one row per voucher line in the report)
-- ----------------------------------------------------------------------------
CREATE TABLE day_book_entries (
    entry_id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    report_id          BIGINT       NOT NULL,
    company_id         BIGINT       NOT NULL,
    voucher_id         BIGINT,
    voucher_date       DATE,                                -- date-only ISO text in source
    particulars        TEXT,
    voucher_type       TEXT,
    voucher_number     TEXT,
    debit_amount       NUMERIC(18,2) NOT NULL DEFAULT 0,    -- MONEY: never float
    credit_amount      NUMERIC(18,2) NOT NULL DEFAULT 0,    -- MONEY: never float
    narration          TEXT,
    party_ledger_name  TEXT,
    show_profit        BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    is_optional        BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    is_post_dated      BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    is_stat_adjustment BOOLEAN      NOT NULL DEFAULT false,  -- SQLite INTEGER 0/1
    gross_profit       NUMERIC(18,2) NOT NULL DEFAULT 0,    -- MONEY: never float
    cost               NUMERIC(18,2) NOT NULL DEFAULT 0,    -- MONEY: never float
    display_order      INTEGER      NOT NULL DEFAULT 0,
    is_drillable       BOOLEAN      NOT NULL DEFAULT true,   -- SQLite INTEGER 0/1, default 1
    notes              TEXT,
    created_at         TIMESTAMPTZ  NOT NULL DEFAULT now(),  -- ISO string in source
    updated_at         TIMESTAMPTZ  NOT NULL DEFAULT now()   -- ISO string in source
);

-- ----------------------------------------------------------------------------
-- day_book_entry_lines  (ledger split lines under an entry)
-- ----------------------------------------------------------------------------
CREATE TABLE day_book_entry_lines (
    line_id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    entry_id       BIGINT       NOT NULL,
    ledger_id      BIGINT,
    particulars    TEXT,
    debit_amount   NUMERIC(18,2) NOT NULL DEFAULT 0,        -- MONEY: never float
    credit_amount  NUMERIC(18,2) NOT NULL DEFAULT 0,        -- MONEY: never float
    line_order     INTEGER      NOT NULL DEFAULT 0,
    notes          TEXT
);

