-- Tables: day_book_reports, day_book_entries, day_book_entry_lines
--
-- Money/quantity columns use NUMERIC, NEVER floating point. The SQLite source
-- declares these as REAL; storing money in REAL/float risks rounding errors.
-- They are mapped to NUMERIC(18,2) (currency) here on purpose.
--
-- SQLite INTEGER 0/1 boolean flags are mapped to BOOLEAN. The application
-- writes 0/1 integers; migrate with: (col <> 0) -> true / (col = 0) -> false.
--
-- SQLite datetime('now') defaults store ISO-8601 strings; mapped to TIMESTAMPTZ
-- with DEFAULT now().
--
-- FK constraints are grouped at the bottom as ALTER TABLE statements so this
-- file can load before/after other modules without ordering problems. FKs that
-- are EXPLICIT in the SQLite source (REFERENCES ...) are marked "explicit"; FKs
-- INFERRED from *_id naming are marked "inferred".
-- ============================================================================

-- ----------------------------------------------------------------------------
-- day_book_reports  (report header)
-- ----------------------------------------------------------------------------
