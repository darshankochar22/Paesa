#!/usr/bin/env node
// ---------------------------------------------------------------------------
// Schema-driven DB docs generator.
//
//   npm run docs:db
//
// Drizzle is the single source of truth for the database schema (the dual
// schema under server/db/schema/{pg,sqlite}). This script introspects the
// Drizzle table objects of BOTH dialects and regenerates:
//
//   docs/db/schema.postgres.generated.sql
//       A Postgres CREATE TABLE contract DERIVED FROM the pgTable schema, with
//       foreign keys emitted as ALTER TABLE ... ADD CONSTRAINT after all tables
//       (so creation order never matters). This is the machine-generated
//       equivalent of the hand-curated docs/db/schema.postgres.sql; it is NOT
//       hand-edited and is safe to regenerate at any time.
//
//   docs/db/SCHEMA.md
//       A human-readable table reference: every table, its columns, the SQLite
//       type and the Postgres type side by side, nullability, defaults, PK and
//       FK targets. Generated straight from the Drizzle metadata.
//
// This generator is independent of the API-docs generator (openapiGenerator.js
// / genStatic.js / checkDocs.js) and does not touch it.
// ---------------------------------------------------------------------------
const fs = require('fs');
const path = require('path');
const { getTableName, getTableColumns, is } = require('drizzle-orm');
const { PgTable } = require('drizzle-orm/pg-core');
const { SQLiteTable } = require('drizzle-orm/sqlite-core');

const pgSchema = require('../db/schema/pg');
const sqliteSchema = require('../db/schema/sqlite');

const DOCS_DB_DIR = path.join(__dirname, '..', '..', 'docs', 'db');
const PG_INLINE_FK = Symbol.for('drizzle:PgInlineForeignKeys');
const SQLITE_INLINE_FK = Symbol.for('drizzle:SQLiteInlineForeignKeys');

// ---------------------------------------------------------------------------
// Introspection helpers.
// ---------------------------------------------------------------------------
function collectTables(schema, tableClass) {
  const out = new Map();
  for (const value of Object.values(schema)) {
    if (!value || !is(value, tableClass)) continue;
    out.set(getTableName(value), value);
  }
  return out;
}

// Render a Drizzle default value back to a printable SQL literal/expression.
function renderDefault(col) {
  if (!col.hasDefault) return null;
  const d = col.default;
  if (d === undefined) return null; // identity / autoincrement — no literal default
  if (d && typeof d === 'object' && Array.isArray(d.queryChunks)) {
    // An sql`` expression — surface the raw chunk text where possible.
    try {
      const chunks = d.queryChunks || [];
      const txt = chunks
        .map((c) => (c && c.value ? (Array.isArray(c.value) ? c.value.join('') : c.value) : ''))
        .join('')
        .trim();
      return txt || '<sql>';
    } catch {
      return '<sql>';
    }
  }
  if (typeof d === 'boolean') return d ? 'TRUE' : 'FALSE';
  if (typeof d === 'string') return `'${d.replace(/'/g, "''")}'`;
  return String(d);
}

// Postgres column type string from pg column metadata.
function pgTypeString(col) {
  switch (col.columnType) {
    case 'PgBigInt53':
      return col.generatedIdentity
        ? 'BIGINT GENERATED BY DEFAULT AS IDENTITY'
        : 'BIGINT';
    case 'PgInteger':
      return 'INTEGER';
    case 'PgBoolean':
      return 'BOOLEAN';
    case 'PgNumeric':
      return `NUMERIC(${col.precision},${col.scale})`;
    case 'PgDoublePrecision':
      return 'DOUBLE PRECISION';
    case 'PgText':
      return 'TEXT';
    case 'PgTimestamp':
      return col.withTimezone ? 'TIMESTAMPTZ' : 'TIMESTAMP';
    case 'PgDateString':
      return 'DATE';
    case 'PgJsonb':
      return 'JSONB';
    default:
      return col.columnType.replace(/^Pg/, '').toUpperCase();
  }
}

// SQLite column type string (the storage class as declared).
function sqliteTypeString(col) {
  switch (col.columnType) {
    case 'SQLiteInteger':
      return col.primary && col.autoIncrement
        ? 'INTEGER PRIMARY KEY AUTOINCREMENT'
        : 'INTEGER';
    case 'SQLiteReal':
      return 'REAL';
    case 'SQLiteText':
      return 'TEXT';
    default:
      return col.columnType.replace(/^SQLite/, '').toUpperCase();
  }
}

// Extract foreign keys declared on a table (inline .references()).
function foreignKeys(table, fkSymbol) {
  const raw = table[fkSymbol];
  if (!raw) return [];
  const fks = [];
  for (const fk of raw) {
    const ref = fk.reference();
    fks.push({
      columns: ref.columns.map((c) => c.name),
      foreignTable: getTableName(ref.foreignTable),
      foreignColumns: ref.foreignColumns.map((c) => c.name),
      onDelete: fk.onDelete || null,
      onUpdate: fk.onUpdate || null,
    });
  }
  return fks;
}

// ---------------------------------------------------------------------------
// Build the introspected model.
// ---------------------------------------------------------------------------
const pgTables = collectTables(pgSchema, PgTable);
const sqliteTables = collectTables(sqliteSchema, SQLiteTable);
const tableNames = [...pgTables.keys()].sort();

// ---------------------------------------------------------------------------
// 1. Generate docs/db/schema.postgres.generated.sql (FROM the pg schema).
// ---------------------------------------------------------------------------
function generatePostgresSql() {
  const lines = [];
  lines.push('-- ============================================================================');
  lines.push('-- POSTGRES SCHEMA — GENERATED FROM DRIZZLE  (server/db/schema/pg/*.js)');
  lines.push('-- ----------------------------------------------------------------------------');
  lines.push('-- Single source of truth: the pgTable definitions under server/db/schema/pg.');
  lines.push('-- Regenerate with:  npm run docs:db');
  lines.push('-- DO NOT HAND-EDIT. Edit the Drizzle schema files instead, then re-run docs:db.');
  lines.push('--');
  lines.push('-- Section 1 — CREATE TABLE (no inline foreign keys).');
  lines.push('-- Section 2 — Foreign keys as ALTER TABLE ... ADD CONSTRAINT.');
  lines.push('-- ============================================================================');
  lines.push('');
  lines.push('-- ############################################################################');
  lines.push('-- SECTION 1 :: CREATE TABLE');
  lines.push('-- ############################################################################');
  lines.push('');

  const allFks = [];

  for (const name of tableNames) {
    const table = pgTables.get(name);
    const cols = getTableColumns(table);
    lines.push(`CREATE TABLE IF NOT EXISTS ${name} (`);
    const colLines = [];
    for (const col of Object.values(cols)) {
      let line = `  ${col.name} ${pgTypeString(col)}`;
      if (col.primary) line += ' PRIMARY KEY';
      if (col.notNull && !col.primary) line += ' NOT NULL';
      const def = renderDefault(col);
      if (def !== null && !col.generatedIdentity) line += ` DEFAULT ${def}`;
      colLines.push(line);
    }
    lines.push(colLines.join(',\n'));
    lines.push(');');
    lines.push('');

    for (const fk of foreignKeys(table, PG_INLINE_FK)) {
      allFks.push({ table: name, ...fk });
    }
  }

  lines.push('-- ############################################################################');
  lines.push('-- SECTION 2 :: FOREIGN KEYS');
  lines.push('-- ############################################################################');
  lines.push('');
  if (allFks.length === 0) {
    lines.push('-- (no inline foreign keys declared in the Drizzle schema)');
  }
  for (const fk of allFks) {
    const cname = `fk_${fk.table}_${fk.columns.join('_')}`;
    let stmt =
      `ALTER TABLE ${fk.table} ADD CONSTRAINT ${cname} ` +
      `FOREIGN KEY (${fk.columns.join(', ')}) ` +
      `REFERENCES ${fk.foreignTable} (${fk.foreignColumns.join(', ')})`;
    if (fk.onDelete && fk.onDelete !== 'no action') {
      stmt += ` ON DELETE ${fk.onDelete.toUpperCase()}`;
    }
    if (fk.onUpdate && fk.onUpdate !== 'no action') {
      stmt += ` ON UPDATE ${fk.onUpdate.toUpperCase()}`;
    }
    stmt += ';';
    lines.push(stmt);
  }
  lines.push('');
  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// 2. Generate docs/db/SCHEMA.md (table reference, both dialects side by side).
// ---------------------------------------------------------------------------
function generateSchemaMd() {
  const lines = [];
  let totalCols = 0;
  let totalFks = 0;

  lines.push('# Database Schema Reference');
  lines.push('');
  lines.push('> **Generated — do not hand-edit.** Run `npm run docs:db` to regenerate.');
  lines.push('>');
  lines.push('> This reference is derived directly from the Drizzle schema, the single');
  lines.push('> source of truth, under `server/db/schema/pg` and `server/db/schema/sqlite`.');
  lines.push('> Column **names** are identical across both dialects (enforced by');
  lines.push('> `npm run db:parity`); the **types** differ by design (SQLite storage');
  lines.push('> classes vs. the Postgres accounting-grade types).');
  lines.push('');

  // Defer the table-of-contents until after we count, but we know names now.
  const toc = ['## Tables', ''];
  for (const name of tableNames) {
    toc.push(`- [\`${name}\`](#${name.replace(/_/g, '-')})`);
  }
  toc.push('');

  const body = [];
  for (const name of tableNames) {
    const pgTable = pgTables.get(name);
    const sqliteTable = sqliteTables.get(name);
    const pgCols = getTableColumns(pgTable);
    const sqliteCols = getTableColumns(sqliteTable);

    const pgFks = foreignKeys(pgTable, PG_INLINE_FK);
    totalFks += pgFks.length;
    const fkByCol = new Map();
    for (const fk of pgFks) {
      for (const c of fk.columns) {
        fkByCol.set(
          c,
          `${fk.foreignTable}(${fk.foreignColumns.join(',')})` +
            (fk.onDelete && fk.onDelete !== 'no action'
              ? ` ON DELETE ${fk.onDelete.toUpperCase()}`
              : '')
        );
      }
    }

    body.push(`### ${name}`);
    body.push('');
    body.push('| Column | SQLite type | Postgres type | Nullable | Key | Default | References |');
    body.push('| --- | --- | --- | --- | --- | --- | --- |');

    // Iterate by SQL column name in pg order; pair with sqlite by SQL name.
    const sqliteByName = new Map(
      Object.values(sqliteCols).map((c) => [c.name, c])
    );
    for (const col of Object.values(pgCols)) {
      totalCols += 1;
      const sqliteCol = sqliteByName.get(col.name);
      const pgType = pgTypeString(col).replace(/ GENERATED BY DEFAULT AS IDENTITY/, ' IDENTITY');
      const sqliteType = sqliteCol
        ? sqliteTypeString(sqliteCol).replace(/ PRIMARY KEY AUTOINCREMENT/, ' (PK, AI)')
        : '—';
      const nullable = col.notNull || col.primary ? 'no' : 'yes';
      const key = col.primary ? 'PK' : fkByCol.has(col.name) ? 'FK' : '';
      let def = renderDefault(col);
      if (col.generatedIdentity) def = 'identity';
      def = def === null ? '' : `\`${def}\``;
      const ref = fkByCol.get(col.name) || '';
      body.push(
        `| \`${col.name}\` | ${sqliteType} | ${pgType} | ${nullable} | ${key} | ${def} | ${ref} |`
      );
    }
    body.push('');
  }

  // Stats summary up top.
  lines.push(
    `**Shape:** ${tableNames.length} tables, ${totalCols} columns, ` +
      `${totalFks} declared foreign keys.`
  );
  lines.push('');
  lines.push(...toc);
  lines.push('---');
  lines.push('');
  lines.push(...body);
  return { md: lines.join('\n'), stats: { tables: tableNames.length, columns: totalCols, fks: totalFks } };
}

// ---------------------------------------------------------------------------
// Write artifacts.
// ---------------------------------------------------------------------------
function main() {
  if (pgTables.size !== sqliteTables.size) {
    console.error(
      `✖ pg/sqlite table count mismatch (${pgTables.size} vs ${sqliteTables.size}). ` +
        `Run npm run db:parity for the diff.`
    );
    process.exit(1);
  }

  const sqlPath = path.join(DOCS_DB_DIR, 'schema.postgres.generated.sql');
  const mdPath = path.join(DOCS_DB_DIR, 'SCHEMA.md');

  const sqlContent = generatePostgresSql();
  const { md, stats } = generateSchemaMd();

  fs.writeFileSync(sqlPath, sqlContent + '\n', 'utf8');
  fs.writeFileSync(mdPath, md + '\n', 'utf8');

  console.log(`✓ docs:db generated from Drizzle schema`);
  console.log(`    ${path.relative(process.cwd(), sqlPath)}`);
  console.log(`    ${path.relative(process.cwd(), mdPath)}`);
  console.log(
    `    ${stats.tables} tables, ${stats.columns} columns, ${stats.fks} foreign keys.`
  );
}

main();
